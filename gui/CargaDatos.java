    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
        String nombreMascota = txtNombre.getText();
        String razaMascota = txtRaza.getText();
        String colorMascota = txtColor.getText();
        String celDueno = txtCelDueno.getText();
        String obsMascota = txtObservaciones.getText();
        String nombreDueno = txtNombreDueño.getText();
        
        String alergico = (String) cmbAlergico.getSelectedItem();
        String ateEsp = (String) cmbAtEsp.getSelectedItem();
        
        control.guardar(nombreMascota, razaMascota, colorMascota, celDueno, obsMascota, nombreDueno, alergico, ateEsp);
    
        mostrarMensaje("Mascota creada exitosamente", "info", "CREAR MASCOTA");
        
    }//GEN-LAST:event_btnGuardarActionPerformed

